    private void pintarUserModel(Usermodel usermodel) throws InternalErrorException_Exception, UnauthorizedException_Exception  {
        titulo.setText(titulo.getText()+" "+usermodel.getId());
        Identificador.setText(Integer.toString(usermodel.getId()));
        Username.setText(usermodel.getUsername()!= null ?usermodel.getUsername():"");
        Nombre.setText(usermodel.getNombre()!= null ?usermodel.getNombre():"");
        Edad.setText(usermodel.getEdad() != null ? Integer.toString(usermodel.getEdad()) : "");
        Email.setText(usermodel.getEmail()!= null ?usermodel.getEmail():"");
        Localizacion.setText(usermodel.getLocalization()!= null ?usermodel.getLocalization():"");
        Boolean cierto = ServiciosUsuarios.isAuthenticated(ventana.getTokenUsuario());
        Autenticado.setText(String.valueOf(cierto));
        
    }


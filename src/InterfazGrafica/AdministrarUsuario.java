    public void refrescarUsuario() {
        try {
            usermodel = ServiciosUsuarios.getUser(usermodel, ventana.getTokenUsuario());
            this.pintarUserModel(usermodel);
        } catch (InternalErrorException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "Error interno",
                        JOptionPane.WARNING_MESSAGE);
        } catch (UnauthorizedException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "No autorizado",
                        JOptionPane.WARNING_MESSAGE);
        } catch (ConnectionException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "Error de conexion",
                        JOptionPane.WARNING_MESSAGE);
        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            int i = JOptionPane.showConfirmDialog(this,
                    "Esta usted seguro de que quiere eliminar el usuario " + usermodel.getNombre() + "?",
                    "Eliminar usuario: " + usermodel.getNombre(),
                    JOptionPane.OK_CANCEL_OPTION);
            if (i == 0) {
                ServiciosUsuarios.deleteUser(usermodel, ventana.getTokenUsuario());
                pintarUserModel(usermodel);
                ventana.obtenerUsuarios();
                this.dispose();
            }
        }  catch (InternalErrorException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "Error interno",
                        JOptionPane.WARNING_MESSAGE);
        } catch (UnauthorizedException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "No autorizado",
                        JOptionPane.WARNING_MESSAGE);
        } catch (ConnectionException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "Error de conexion",
                        JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Integer id = Integer.parseInt(Identificador.getText());
        String username = Username.getText();
        String nombre = Nombre.getText();
        String email = Email.getText();
        if (id != null
                && username != null
                && !username.isEmpty()
                && nombre != null && !nombre.isEmpty()
                && email != null && !email.isEmpty()) {
            usermodel.setUsername(username);
            usermodel.setId(id);
            usermodel.setNombre(nombre);
            usermodel.setEmail(email);
            try {
                int updateUser = ServiciosUsuarios.updateUser(usermodel, ventana.getTokenUsuario());
                if (updateUser == 1) {
                    JOptionPane.showMessageDialog(this,
                            "El usuario ha sido actualizado con exito",
                            "Actualizado",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Error actualizando el usuario seleccionado",
                            "Error de actualizado",
                            JOptionPane.WARNING_MESSAGE);
                }
            } catch (InternalErrorException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "Error interno",
                        JOptionPane.WARNING_MESSAGE);
        } catch (UnauthorizedException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "No autorizado",
                        JOptionPane.WARNING_MESSAGE);
        } catch (ConnectionException_Exception ex) {
           JOptionPane.showMessageDialog(this,
                        ex.getMessage(),
                        "Error de conexion",
                        JOptionPane.WARNING_MESSAGE);
        }

        } else {
            JOptionPane.showMessageDialog(this,
                    "No se han introducido correctamente los datos por lo que no se ha actualizado",
                    "Error en los datos",
                    JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

